#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>
#include <string.h>

/* 재귀함수로 언더바 출력 */
int printstr(int num)
{
	if (num <= 0) return 0;
	printf("____");
	return printstr(num - 1);
}

/* 재귀함수, 0부터 s까지 오름 */
int recursive(int num, int s)
{
	printstr(num);
	printf("\"재귀함수가 뭔가요?\"\n");
	if (num >= s)	// 재귀함수 멈추는 트리거
	{
		printstr(num);
		printf("\"재귀함수는 자기 자신을 호출하는 함수라네\"\n");
		printstr(num);
		printf("라고 답변하였지.\n");
		return 1;	// 다시 돌아가기
	}
	printstr(num);
	printf("\"잘 들어보게. 옛날옛날 한 산 꼭대기에 이세상 모든 지식을 통달한 선인이 있었어.\n");
	printstr(num);
	printf("마을 사람들은 모두 그 선인에게 수많은 질문을 했고, 모두 지혜롭게 대답해 주었지.\n");
	printstr(num);
	printf("그의 답은 대부분 옳았다고 하네. 그런데 어느 날, 그 선인에게 한 선비가 찾아와서 물었어.\"\n");
	return recursive(num + 1, s) + printstr(num) + printf("라고 답변하였지.\n");
}

int main()
{
	
	int num = 0;
	int s;
	scanf("%d", &s);
	printf("어느 한 컴퓨터공학과 학생이 유명한 교수님을 찾아가 물었다.\n");
	recursive(num, s);
	return 0;
}